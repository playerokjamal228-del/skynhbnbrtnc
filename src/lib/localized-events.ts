function mapLocalizedEvent(event: EventData, locale: Locale): EventData {
  const override = localizedEvents[locale]?.[event.slug];

  if (!override) {
    return event;
  }

  return {
    ...event,
    title: override.title,
    excerpt: override.excerpt,
    dateLabel: override.dateLabel,
    location: override.location,
    newsLabel: override.newsLabel,
    summary: override.summary,
    highlights: override.highlights,
    details: override.details,
    sections: override.sections,
    faq: override.faq,
    originalStatusNote: override.originalStatusNote ?? event.originalStatusNote,
  };
}

export function getLocalizedEvent(event: EventData, locale: Locale) {
  return mapLocalizedEvent(event, locale);
}

export function getLocalizedSortedEvents(locale: Locale) {
  return sortedEventData.map((event) => mapLocalizedEvent(event, locale));
}

export function getLocalizedHomeEventHighlights(locale: Locale) {
  return getLocalizedSortedEvents(locale).slice(0, 6).map((event, index) => ({
    id: `${index + 1}-${event.slug}`,
    category: event.category,
    badge: event.badge,
    title: event.title,
    date: event.dateLabel,
    image: event.image,
    excerpt: event.excerpt,
    href: `/events/${event.slug}`,
  }));
}

export function getLocalizedEventNewsItems(locale: Locale) {
  return getLocalizedSortedEvents(locale).map((event) => ({
    label: event.newsLabel,
    title: event.title,
    href: `/events/${event.slug}`,
  }));
}
